#include <p4.h>

t_p4		*ft_copy_p4(t_p4 *p)
{
	t_p4	*cpy;
	int		i;
	int		j;

	cpy = ft_create_p4(p->h, p->w);
	if (cpy)
	{
		i = 0;
		while (i < p->h)
		{
			j = 0;
			while (j < p->w)
			{
				cpy->map[i][j] = p->map[i][j];
				j++;
			}
			i++;
		}
	}
	return (cpy);
}
